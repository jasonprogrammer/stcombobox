function CreateCollegesCombo() {
    var combo = new STComboBox();
    combo.Init('collegesCombo');
    var data = [];

    var colleges = GetColleges();

    for(var i=0; i < colleges.length; i++) {
        data.push({id: i, text: colleges[i]});
    }
    combo.populateList(data);
}

